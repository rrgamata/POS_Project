    private void AddClientOkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddClientOkButtonActionPerformed
        // TODO add your handling code here:
        if(!checkClient(clientNameTextField.getText(), clientAddressTextArea.getText(),  clientPhoneTextField1.getText())){
            if(editFlg){
                int result = ClientAction.editClient(clientNameTextField.getText(), clientAddressTextArea.getText(),  clientPhoneTextField1.getText(), clientNumber, 2, clientNumberTextField.getText(), "0.0",0);
                if(!(result < 0) ){
                    JOptionPane.showMessageDialog(this,ErrorMessages.CONFIRM_EDITED_CLIENT ,ErrorMessages.TITLE_SUCCESS,JOptionPane.INFORMATION_MESSAGE);
                }
                this.dispose();
                
            } else {
                int result = ClientAction.insertClient(clientNameTextField.getText(), clientAddressTextArea.getText(), clientPhoneTextField1.getText(), 2,clientNumberTextField.getText(), "0.0",0);
                if(!(result < 0) ){
                    success();
                }
            }
        }
}//GEN-LAST:event_AddClientOkButtonActionPerformed

