    private void AddClientOkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddClientOkButtonActionPerformed
        // TODO add your handling code here:
        if(!checkClient(locationNameTextField.getText(), locationAddressTextArea.getText(), locationPhoneTextField.getText())){
            if(editFlg){
                int result = LocationAction.editLocation(locationNameTextField.getText(), locationAddressTextArea.getText(), locationPhoneTextField.getText(), clientNumber);
                if(!(result < 0) ){
                    JOptionPane.showMessageDialog(this,ErrorMessages.CONFIRM_EDITED_CLIENT ,ErrorMessages.TITLE_SUCCESS,JOptionPane.INFORMATION_MESSAGE);
                }
                this.dispose();
                
            } else {
                int result = LocationAction.insertLocation(locationNameTextField.getText(), locationAddressTextArea.getText(), locationPhoneTextField.getText());
                if(!(result < 0) ){
                    success();
                }
            }
        }
}//GEN-LAST:event_AddClientOkButtonActionPerformed

