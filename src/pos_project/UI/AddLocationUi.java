    private boolean checkClient(String name, String address, String phone){
        boolean errorFlg = false;
        String errMsg = new String();
        if(name.isEmpty()||name == null){
            errorFlg = true;
            errMsg += "* "+ErrorMessages.ERROR_CLIENT_NAME;
        }
        if(address.isEmpty()||address == null){
            errorFlg = true;
            errMsg += "* "+ErrorMessages.ERROR_CLIENT_ADDRESS;
        }
        if(phone.isEmpty()||phone == null){
            errorFlg = true;
            errMsg += "* "+ErrorMessages.ERROR_CLIENT_PHONE;
        }
        if(errorFlg){
            
            JOptionPane.showMessageDialog(this,errMsg ,ErrorMessages.TITLE_ERROR,JOptionPane.ERROR_MESSAGE);
        }
        
        return errorFlg;
        
        
    }
     
     private void success(){
          int answer = JOptionPane.showConfirmDialog(this,ErrorMessages.CONFIRM_ADDED_LOCATION,ErrorMessages.TITLE_SUCCESS,JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
          if(answer==0){
             locationNameTextField.requestFocus();
             locationNameTextField.setText("");
             locationAddressTextArea.setText("");
             locationPhoneTextField.setText("");
          }else{
             this.dispose();
         }
     }
