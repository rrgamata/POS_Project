    private void initializeTable(){
        userTable.getTableHeader().setReorderingAllowed(false);
        DefaultTableModel mod = LocationAction.getMainTableData();
        
         txtSearch.addKeyListener(new KeyListener(){
            public void keyReleased(java.awt.event.KeyEvent e){
                String text = txtSearch.getText();
                if (text.length() == 0) {
                    sorter.setRowFilter(null);
                } else {
                    sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text));
                }
            }

            public void keyPressed(java.awt.event.KeyEvent e){
            }

            public void keyTyped(java.awt.event.KeyEvent e){
                
            }

            });
        sorter = new TableRowSorter<DefaultTableModel>(mod);
        userTable.setModel(mod);
        userTable.setRowSorter(sorter);
        userTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        userTable.setShowGrid(true);
        userTable.setGridColor(Color.black);
        userTable.getColumnModel().removeColumn(userTable.getColumnModel().getColumn(3));
        System.out.println(userTable.getModel());
    }

