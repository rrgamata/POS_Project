    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (userTable.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this,ErrorMessages.ERROR_NO_SELECTED_LOCATION ,ErrorMessages.TITLE_ERROR,JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, ErrorMessages.CONFIRM_DELETE_LOCATION, ErrorMessages.TITLE_DELETE, JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
                      User.deleteUser(userTable.getModel().getValueAt(userTable.convertRowIndexToModel(userTable.getSelectedRow()), 3).toString());
                      initializeTable();
            }
            
//         initializeTable(); 
        }
    }//GEN-LAST:event_jButton3ActionPerformed

